#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	long	a;

	a = nb;
	if (a < 0)
	{
		ft_putchar('-');
		a = -a;
	}
	if (a > 9)
	{
		ft_putnbr(a / 10);
		a = a % 10;
	}
	if (a < 10)
	{
		ft_putchar(a + '0');
	}
}
